package forCondition;

import java.util.Scanner;

public class _2742 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int n;
        n = sc.nextInt();

        for (int i = n; i >= 1; i--) {
            System.out.println(i);
        }

        sc.close();
    }
}
